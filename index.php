Agriculture remains the most important sector of Bangladeshi economy, contributing 19.6 percent to the national GDP and providing employment for 63 percent of the population. Agriculture in Bangladesh is heavily dependent on the weather, and the entire harvest can be wiped out in a matter of hours when cyclones hit the country. According to the World Bank, the total arable land in Bangladesh is 61.2 percent of the total land area (down from 68.3 percent in 1980). Farms are usually very small due to heavily increasing population, unwieldy land ownership, and inheritance regulations. The 3 main crops—rice, jute, and tea—have dominated agricultural exports for decades, although the rice is grown almost entirely for domestic consumption, while jute and tea are the main export earners. In addition to these products, Bangladeshi farmers produce sugarcane, tobacco, cotton, and various fruits and vegetables (sweet potatoes, bananas, pineapples, etc.) for the domestic market.</p><p>

Rice is the staple food in the everyday diet of Bangladeshis. The production of rice, which can be harvested 2 or even 3 times a year, reached 19.9 million metric tons in 1998-99. The production of wheat reached about 2 million metric tons in 1998-99. Both crops play an important role in achieving self-sufficiency in food production. However, due to weather conditions the production of rice and wheat fluctuate greatly, forcing Bangladesh to import food from the international market or turn to international aid. Bangladesh imported 1.6 million tons of wheat (mainly from the United States) in 2000 in order to meet the demand in the local market.</p><p>

Jute, often called the "golden fibre" of Bengal, is the main export-earner for Bangladeshi agriculture, as Bangladesh remains the world's second-largest producer of jute (after India) and the world's largest exporter of fiber. Jute is traditionally used for the fiber of carpet backing, burlap bags, cheap paper, and various other purposes. Its importance for the Bangladeshi economy comes from the fact that almost 3 million farms are involved in jute production. In 1999 Bangladeshi export earnings from jute amounted to US$55 million, with the country producing 720,000 metric tons of jute, although this is about one-third of the jute production of the middle of the 1980s. The decline in jute production is attributed to declining world prices for this crop and to farmers switching to other crops.</p><p>

Bangladesh also produces tea leaves, mainly for export, although the export of this product contributes only 1 percent of the country's hard currency earnings. In 1998-99 the country produced 56,000 metric tons of tea leaves, but it could produce twice that amount. The main obstacle to increasing production is in falling prices for tea in the international market and in management and regulation problems in the industry in the country.</p><p>

Tropical rainforest is important for maintaining the ecological balance in Bangladesh, and forestry contributes 1.9 percent to the GDP (1999-2000). The forest covers around 17 percent of the country's territory, or 2.5 million hectares (6.18 million acres). The timber is used by the construction industry as a source of building materials, by the printing industry as a source of materials to produce paper, and in the agricultural sector as a source of firewood. Commercial logging is limited to around 6.1 million cubic feet, and the government plans to plant more trees within the next 15 years.</p><p>

Fishing is another important activity in the country, contributing 4.9 percent to the GDP (1999-2000) and providing 6 percent of the total export income. The overall fish production was around 1.6 million metric tons (1999-2000). Bangladesh mainly exports its shrimp to the international market.</p></font>

